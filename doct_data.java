            pst.setString(2,fnam);
            pst.setString(3,lnam);
            pst.setString(4,tel1);
            pst.setString(5,sex);
            pst.setString(6,clin);
            pst.setString(7,adt);
            pst.setString(8,atim);
            pst.setString(9,pss1);
            pst.setString(10,cval);
            pst.execute();
