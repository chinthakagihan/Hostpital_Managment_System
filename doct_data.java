            Did.setText("");
            fname.setText("");
            lname.setText("");
            tel.setText("");
            gen.setSelectedIndex(0);
            clinic.setSelectedIndex(0);
            Adate.setSelectedIndex(0);
            atime.setText("");
            cvalues.setText("");
            passwd.setText("");
            passwd1.setText("");
